def to_bold(text: str) -> str:
    return ''.join(_BOLD_MAP.get(c, c) for c in text)


def to_italic(text: str) -> str:
    return ''.join(_ITALIC_MAP.get(c, c) for c in text)


def to_regular(text: str) -> str:
    return ''.join(_REGULAR_MAP.get(c, c) for c in text)


# ── Shared helpers ─────────────────────────────────────────────────────────────

def _clean_hashtags(tags: list[str]) -> list[str]:
    return [t.replace(' ', '').replace('-', '').lower() for t in tags if t.strip()]


def _render_sections(sections: list[dict]) -> list[str]:
    lines: list[str] = []
    for i, sec in enumerate(sections):
        if i > 0:
            lines += ['', '• • •', '']
        else:
            lines.append('')

        name     = (sec.get('name')     or '').strip()
        dates    = (sec.get('dates')    or '').strip()
        tags     = (sec.get('tags')     or '').strip()
        body     = (sec.get('body')     or '').strip()
        schedule = (sec.get('schedule') or '').strip()

        if name:
            lines.append(to_bold(name))
        if dates:
            lines.append(to_bold(dates))
        for tag_line in tags.split('\n'):
            if tag_line.strip():
                lines.append(to_italic(tag_line.strip()))
        if body:
            lines += ['', to_regular(body)]
        if schedule:
            lines.append('')
            for sched_line in schedule.split('\n'):
                if sched_line.strip():
                    lines.append(to_bold(sched_line.strip()))
                else:
                    lines.append('')
    return lines


def _render_about(label, name, bio) -> list[str]:
    if not any([label, name, bio]):
        return []
    lines = ['']
    if label:
        lines.append(to_bold(label))
    bio_lines = (bio or '').split('\n')
    first = bio_lines[0].strip() if bio_lines else ''
    rest  = [l for l in bio_lines[1:] if l.strip()]
    if name:
        lines.append(
            to_regular(name) + ' ' + to_italic(first) if first else to_regular(name)
        )
    elif first:
        lines.append(to_italic(first))
    for line in rest:
        lines.append(to_italic(line))
    return lines


# ── Telegram ──────────────────────────────────────────────────────────────────

def format_for_telegram(
    hook: str,
    cta: str = '',
    sections: list[dict] | None = None,
    about_label: str | None = None,
    about_name: str | None = None,
    about_bio: str | None = None,
    early_bird: str | None = None,
    hashtags: list[str] | None = None,
    link: str | None = None,
    **kwargs,
) -> str:
    lines: list[str] = [to_bold(hook)]

    lines += _render_sections(sections or [])
    lines += _render_about(about_label, about_name, about_bio)

    # Early Bird
    if early_bird:
        eb = early_bird.split('\n')
        lines += ['', f'\u25c6  {to_bold(eb[0].strip())}  \u25c6']
        if len(eb) > 1 and eb[1].strip():
            lines.append(eb[1].strip())

    # Registration link — clickable Markdown syntax
    if link:
        lines += ['', f'[\u2726 REGISTRATION \u2726]({link})']

    # CTA
    if cta:
        lines += ['', cta]

    # Hashtags — max 5, all on one line for Telegram
    if hashtags:
        cleaned = _clean_hashtags(hashtags)[:5]
        lines += ['', ' '.join(f'#{t}' for t in cleaned)]

    return '\n'.join(lines)


# ── Instagram ─────────────────────────────────────────────────────────────────

def format_for_instagram(
    hook: str,
    cta: str = '',
    sections: list[dict] | None = None,
    about_label: str | None = None,
    about_name: str | None = None,
    about_bio: str | None = None,
    early_bird: str | None = None,
    hashtags: list[str] | None = None,
    link: str | None = None,
    **kwargs,
) -> str:
    lines: list[str] = [to_bold(hook), '', '\u2726 \u2726 \u2726']

    lines += _render_sections(sections or [])
    lines += _render_about(about_label, about_name, about_bio)

    # Early Bird
    if early_bird:
        eb = early_bird.split('\n')
        lines += ['', f'\u25c6  {to_bold(eb[0].strip())}  \u25c6']
        if len(eb) > 1 and eb[1].strip():
            lines.append(to_regular(eb[1].strip()))

    # Registration link
    if link:
        lines += ['', '\u2726 REGISTRATION \u2726', link]

    # CTA
    if cta:
        lines += ['', to_regular(cta)]

    # Hashtags — exactly 5 most powerful, each on its own line
    if hashtags:
        cleaned = _clean_hashtags(hashtags)[:5]
        lines.append('')
        for tag in cleaned:
            lines.append(f'#{tag}')

    return '\n'.join(lines)


# ── YouTube ───────────────────────────────────────────────────────────────────

def format_for_youtube(
    hook: str,
    youtube_data: dict | None = None,
    hashtags: list[str] | None = None,
    **kwargs,
) -> str:
    yt = youtube_data or {}
    lines: list[str] = [to_bold(hook), '']

    # Description
    description = (yt.get('description') or '').strip()
    if description:
        lines += [description, '']

    # Ideal for
    ideal_for = yt.get('ideal_for') or []
    if ideal_for:
        lines.append(to_bold('Ideal for:'))
        for item in ideal_for:
            lines.append(f'[ {item} ]')
        lines.append('')

    # Timecodes / Chapters
    timecodes = (yt.get('timecodes') or '').strip()
    if timecodes:
        lines += [to_bold('\U0001f3ac Chapters:')]
        for tc_line in timecodes.split('\n'):
            if tc_line.strip():
                lines.append(tc_line.strip())
        lines.append('')

    # Speaker
    speaker_name = (yt.get('speaker_name') or '').strip()
    speaker_bio  = (yt.get('speaker_bio') or '').strip()
    speaker_web  = (yt.get('speaker_website') or '').strip()
    speaker_ig   = (yt.get('speaker_instagram') or '').strip()
    if speaker_name:
        lines.append(to_bold('Speaker:'))
        lines.append(speaker_name)
        if speaker_bio:
            lines.append(speaker_bio)
        if speaker_web:
            lines.append(f'Website: {speaker_web}')
        if speaker_ig:
            lines.append(f'Instagram: {speaker_ig}')
        lines.append('')

    # Produced by
    produced_by = (yt.get('produced_by') or '').strip()
    if produced_by:
        lines.append(to_bold('Produced by'))
        lines.append(produced_by)
        lines.append('https://sredaplayground.com/')
        lines.append('@sredaplayground')
        lines.append('')

    # Collaborators
    collaborators = yt.get('collaborators') or []
    if collaborators:
        lines.append(to_bold('In Collaboration with'))
        for collab in collaborators:
            name = collab.get('name', '')
            ig   = collab.get('instagram', '')
            if ig:
                lines.append(f'{name}  {ig}')
            else:
                lines.append(name)
        lines.append('')

    # Credits
    filmed_by = (yt.get('filmed_by') or '').strip()
    edited_by = (yt.get('edited_by') or '').strip()
    if filmed_by:
        lines.append(f'{to_bold("Filmed by")} {filmed_by}.')
    if edited_by:
        lines.append(f'{to_bold("Edited by")} {edited_by}.')
    if filmed_by or edited_by:
        lines.append('')

    # Music
    music = yt.get('music') or []
    if music:
        lines.append(to_bold('Music:'))
        for track in music:
            lines.append(track)
        lines.append('')

    # Hashtags
    if hashtags:
        cleaned = _clean_hashtags(hashtags)
        lines.append(' '.join(f'#{t}' for t in cleaned))

    return '\n'.join(lines)
